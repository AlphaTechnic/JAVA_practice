package org.opentutorials.javatutorials.variables;

public class IntvalDoubleval {
    public static void main(String[] args){
        int a;
        a = 1;
        System.out.println(a + 1);
        System.out.println(a + 1.2);

        double b;
        b = 1.1;
        System.out.println(b + 1.2);
    }
}
